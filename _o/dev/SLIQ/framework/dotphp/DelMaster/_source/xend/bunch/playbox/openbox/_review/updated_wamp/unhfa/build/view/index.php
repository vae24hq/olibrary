    <h2><strong>Protecting civilians, monitoring human rights and supporting implementation of cessation of hostilities agreement</strong></h2>
    
    <p>On 9 July 2011 South Sudan became the  newest country in the world. The birth of the Republic of South Sudan  is the culmination of a six-year peace process which began with the signing of  the Comprehensive Peace Agreement (CPA) in 2005.</p>
    <p>In  adopting <a href="#">resolution 1996 (2011)</a> <img src="<?php echo path_to('icon');?>pdf.gif" alt="PDF Document" border="0" height="14" width="14"> on 8 July 2011, the Security Council determined  that the situation faced by South Sudan continued to constitute a threat to  international peace and security in the region and established the United  Nations Mission in the Republic of South Sudan (UNMISS) to consolidate peace and security and to help establish conditions for  development.</p>
    <p>Following  the crisis which broke out in South Sudan in December 2013, the Security  Council, by its <u><a href="#">resolution  2155</a></u><a href="#"><u> (2014</u>)</a> of 27 May 2014, reinforced UNMISS and reprioritized  its mandate towards the protection of civilians, human rights monitoring,  and support for the delivery of humanitarian assistance  and for the implementation of the Cessation of Hostilities  Agreement. </p>
