<p>
<img src="asset/sir-hodge.jpg" title="Sir Julian Hodge" class="left">
所有这三个机构都是由朱利安·霍奇爵士（Julian Hodge）爵士创建的，他们自1950年代以来的活动得到广泛的认可，这已经成为现在在南威尔士存在的动态金融服务行业发展的催化剂。</p>

<p class="paragraph">由Julian Hodge爵士设立的两个早前的金融机构现在由主要的上市公司所有，但是<?php echo quin::$name;?>仍然是一家独立的家族控制公司，它的文化和哲学的关键在于它与许多其他金融机构分开。</p>

<p class="paragraph">在成立之前，自1970年代以来以凯雷（Carlyle）名义交易的集团已经成功开发了几家金融服务公司，这些公司已经在朱利安·霍奇（<?php echo quin::$name;?>）的旗下聚集在一起。</p>

<p class="paragraph">自那以前，银行一直奉行谨慎和有控制的发展政策，长期以来，不需要采取经常与上市公司相关的短期战略。 这种方法确保了集团在90年代初的衰退期间表现良好，并创造了一个坚实的平台，可以在所选择的业务领域内开展业务关系。</p>

<p class="paragraph">
<img src="asset/joe-hodge.jpg" class="right">
我们企业理念的核心原则是决心促进和维系与客户的个人关系，同时真正关心与所有与之有业务关系的人的福利。 口音在于专业精神，与我们运营的市场，诚信和效率以及创新能力相结合。</p>

<p class="paragraph">这样的理想在当今复杂的世界中并不总是很容易遵守，只有通过会议，或者最好超过客户的期望才能取得成功。 每个业务关系中个人层面的良好沟通是必不可少的组成部分。</p>

<p class="paragraph">除了其业务目标外，该集团还承认社会责任，并将继续为福利，医疗，学术（<?php echo paragaph_link('jhiam','Julian Hodge Institute of Applied Macroeconomics');?>）和教育问题提供财政支持。 通常，这是通过简爱基金会或朱利安霍奇慈善信托基金会的慈善机构，共同拥有母公司凯雷信托有限公司近80％的普通股权。.</p>