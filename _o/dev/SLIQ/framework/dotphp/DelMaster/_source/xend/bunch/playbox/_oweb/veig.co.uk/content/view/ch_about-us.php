<h4 class="subheading">在财务方面，<?php echo quin::$name;?>是一家年轻的机构，在1987年获得了“资金法”的授权状态。但是，这一点令人沮丧，因为它是从创建特许信托公司等同一家公司培育出来的。</h4>
