<h2 class="heading">Préstamos y depósitos comerciales</h2>
<h4 class="subheading"><?php echo quin::$name;?> es una institución financiera privada e independiente con sede en Cardiff, aunque nuestra base de clientes comerciales se extiende más allá de Gales, a través del suroeste, Londres, los Home Counties y Midlands.</h4>
</hgroup>

<p>Las tres instituciones fueron fundadas por Sir Julian Hodge, cuyas actividades desde la década de 1950 son ampliamente reconocidas por haber sido el catalizador detrás del desarrollo del sector dinámico de servicios financieros que ahora existe en el sur de Gales.</p>

<p class="paragraph">Ofrecemos una relación de estilo de banco privado para satisfacer las necesidades de financiamiento de bienes raíces y energía renovable de nuestros clientes corporativos, inversores y desarrolladores.</p>

<p class="paragraph">Con un patrimonio de 30 años y una toma de decisiones cercana a la "línea de frente", tenemos la experiencia necesaria para evaluar y responder rápidamente a su propuesta.</p>

<p class="paragraph">Uno de los pocos enfocados principalmente en el sector inmobiliario normalmente prestamos dentro del rango de £ 1m - £ 25m y brindamos líneas de financiamiento en todo el espectro de propiedades de inversión, hipotecas comerciales y desarrollo, así como para proyectos de energía renovable.</p>

<p class="paragraph">El foco central de <?php echo quin::$name;?> es ser un socio de préstamos a largo plazo. También proporcionamos otros servicios auxiliares. Como tal, a menudo podemos complementar o reemplazar los arreglos bancarios existentes de un cliente al otorgar préstamos a plazos estructurados discretos mientras se mantiene la relación de compensación.</p>

<p class="paragraph">También tenemos una excelente reputación por ofrecer una gama competitiva de cuentas de depósito para clientes comerciales y privados.</p>

